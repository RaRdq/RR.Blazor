namespace RR.Blazor.Enums;

public enum DropdownDirection
{
    Auto,       // Smart detection based on available space
    Up,         // Force dropdown to open upward
    Down,       // Force dropdown to open downward
    Left,       // Force dropdown to open leftward
    Right,      // Force dropdown to open rightward
    UpLeft,     // Force dropdown to open up and to the left
    UpRight,    // Force dropdown to open up and to the right
    DownLeft,   // Force dropdown to open down and to the left
    DownRight   // Force dropdown to open down and to the right
}